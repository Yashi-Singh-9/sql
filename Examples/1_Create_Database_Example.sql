-- Create Database
CREATE DATABASE company_db;

-- Why do we create a database first?
-- A database is a structured place where data is stored and organized.
-- We create a database first because it acts as the main container
-- that holds tables, records, and other database objects.

-- Real Life Analogy:
-- Library      -> Database
-- Bookshelves  -> Tables
-- Books        -> Records (Rows)

-- Before creating tables or storing data,
-- we must create the database container.

-- Example:
-- CREATE DATABASE company_db;

-- This creates a database where we can store tables
-- like employees, customers, orders, etc.

-- Reasons for creating a database first:
-- 1. Organization  : Keeps data structured
-- 2. Security      : Databases support access control
-- 3. Performance   : Databases manage data efficiently
-- 4. Separation    : Different applications can use different databases